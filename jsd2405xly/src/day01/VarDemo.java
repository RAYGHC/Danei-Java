public class VarDemo {

    public static void main(String[] args) {
        /*
        变量的语法：
        数据类型 变量名 = 数据;
        int a = 250;
        变量的注意事项：
        1）变量的值可以修改
        2)对变量的使用，就是对它所存那个数的使用
         */
        int a = 250; //声明一个整型的变量a,并赋值250(assignment)
        int b = 5, c = 10, d = 100;//声明3个整型变量b,c,d,并赋值5，10，100
        b = 55;
        int e = c + 50;//声明整型变量 并赋值为c+50
    }
}
